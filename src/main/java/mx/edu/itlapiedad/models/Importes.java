package mx.edu.itlapiedad.models;

public class Importes {

	private int cajero_id;
	private float importe;
	private String cajero_nombre;
	public int getCajero_id() {
		return cajero_id;
	}
	public void setCajero_id(int cajero_id) {
		this.cajero_id = cajero_id;
	}
	public float getImporte() {
		return importe;
	}
	public void setImporte(float importe) {
		this.importe = importe;
	}
	public String getCajero_nombre() {
		return cajero_nombre;
	}
	public void setCajero_nombre(String cajero_nombre) {
		this.cajero_nombre = cajero_nombre;
	}
	
	
}
